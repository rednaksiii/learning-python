class Computer:
    def __init__(self):
        # private attribute
        self.__maxprice = 900

    def sell(self):
        print("Selling Price: {}".format(self.__maxprice))

    def setMaxPrice(self, price):
        self.__maxprice = price

c = Computer()
c.sell()

# change the price
c.setMaxPrice(1000)
c.sell()